def unwrap(obj):
    #if isinstance(obj, YAMLDictInner):
    #    return {k: unwrap(v) for k, v in obj.items()}
    if isinstance(obj, dict):
        return {k: unwrap(v) for k, v in obj.items()}
    elif isinstance(obj, Path):
        return str(obj)
    elif isinstance(obj, list):
        return [unwrap(o) for o in obj]
    else:
        return obj

def _get_provenance(output_dir):
    filepath = Path(output_dir) / 'provenance.yaml'
    if os.path.exists(filepath):
        with open(filepath, "r") as f:
            provenance = defaultdict(lambda : None, yaml.safe_load(f))
    else:
        provenance = defaultdict(lambda : None)

    return provenance    

def _save_provenance(provenance):
    filepath = Path(provenance['output_dir']) / 'provenance.yaml'
    with open(filepath, "w") as f:
        yaml.safe_dump(unwrap(provenance), f, default_flow_style=False)


def init(output_dir=None): 
    if output_dir is None:
        output_dir = "run-" + datetime.now().strftime('%m-%d-%H%M%S')
    
    output_dir = Path(output_dir)    
    output_dir.mkdir(exist_ok=True)

    provenance = _get_provenance(output_dir)
    if 'output_dir' not in provenance:
        provenance['output_dir'] = output_dir

    _save_provenance(provenance)
    return provenance



