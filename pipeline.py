def _save_rigid_motion_correction_video(output_dir, func_ch, func_ch_file, z, path, anchor=(50,50), wd=0, 
                                        ht=0, t_stim_st=31, t_stim_end=211, ds_ratio=0.5, speed_up=20, frame_period=0.585):

    """
    "anchor":  # where to anchor the stim bar    
    "wd" : # width and height of the stim bar
    "ht": 
    "t_stim_st" : # time in seconds of the start of the stim
    "t_stim_end" :  # time in seconds of the end of the stim
    "ds_ratio":   # the fraction of frames to show in the video
    "speed_up":    # how much to speed up the video by
    "frame_period":  # time in seconds between frames, get this from the xml file
    """

    import imageio 

    fr = 1/frame_period
    fps = speed_up * fr * ds_ratio

    fr_stim_st = int(ds_ratio * t_stim_st)
    fr_stim_end = int(ds_ratio * t_stim_end)

    mv = cm.load(func_ch_file).resize(1, 1, ds_ratio)
    _min, _max = np.percentile(mv, 0.01), np.percentile(mv, 99.99)
    
    mv = np.array(255 * (mv - _min)/(1.1 * (_max - _min)), dtype = 'uint8')
    mv = mv[:,:,:,None] * np.ones(mv.shape + (3,))
    
    mcvid_filename = output_dir / f'{path}_{func_ch}_{speed_up}x-rigcorr_video.mp4'
    mv[fr_stim_st:fr_stim_end, anchor[0]:anchor[0] + ht, anchor[1]:anchor[1] + wd, 0] = 255
    imageio.mimwrite(mcvid_filename, mv.astype('uint8'), fps = fps,  quality=7) ###NOTE pip install imageio-ffmpeg


def _visualize_rigcorr_results(mc, mc_ch, mc_ch_file, figbasename):

    #1. 
    plt.figure()
    plt.plot(np.array(mc.shifts_rig)[:,0], label = 'x shifts')
    plt.plot(np.array(mc.shifts_rig)[:,1], label = 'y shifts')
    plt.legend()
    plt.xlabel('frames')
    plt.ylabel('pixels')

    plt.savefig(str(figbasename) + '_rigid-shifts.pdf')

    #2. 
    if mc.pw_rigid:
        _,ax=plt.subplots(2,1)
        ax[0].plot(np.array(mc.x_shifts_els) - np.array(mc.shifts_rig)[:,0,None])
        ax[0].set_title('x shift deviations')
        ax[1].plot(np.array(mc.y_shifts_els) - np.array(mc.shifts_rig)[:,1,None])
        ax[1].set_title('y shift deviations')

        plt.savefig(str(figbasename) + '_shift-deviations.pdf')
        
    
    #3. 
    corr2 = np.array(cm.load(mc.mmap_file))
    raw2 = np.array(cm.load_movie_chain([mc_ch_file]))

    c = corr2.reshape(corr2.shape[0], -1)
    r = raw2.reshape(raw2.shape[0], -1)

    def corr_m(x):
        xm = x.mean(axis=0)
        xm_ = xm - xm.mean()
        x_ = x - x.mean(axis=1)[:,None]
        corr = (x_ @ xm_) / np.sqrt((x_ @ x_.T).diagonal() * (xm_**2).sum())
        return corr

    c = corr_m(c)
    r = corr_m(r)

    fig, ax = plt.subplots(1,2, figsize = (10,5))
    ax[0].plot(c, label = 'corrected')
    ax[0].plot(r, label = 'raw')
    ax[0].set(xlabel = 'Frame', ylabel = 'Correlation with Mean')
    ax[0].legend()

    ax[1].scatter(x = r, y= c)
    xlim = ax[1].get_xlim()
    ylim = ax[1].get_ylim()
    nlim = ( min(xlim[0], ylim[0]), max(xlim[1], ylim[1]) )
    ax[1].plot(nlim, nlim, color = 'k', ls = '--')
    ax[1].set( ylabel = 'Corrected', xlabel = 'Raw', 
            title = 'Correlation with Mean', 
            xlim = nlim, ylim = nlim)

    sns.despine()
    fig.tight_layout(pad = 1.)
    fig.savefig(str(figbasename) + '_correlation-with-mean.pdf')



