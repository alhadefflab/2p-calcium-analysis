def restore_affine_correction(provenance, z):
    affcorr_results_filenames = provenance['affine_motion_correction'][z]['filenames']
    affcorr_results = copy.deepcopy(affcorr_results_filenames)

    def replace_filenames_with_results(results):
        for k, v in results.items():
            if isinstance(v, dict):
                results[k] = replace_filenames_with_results(v)
            else:
                results[k] = imread(v).squeeze()

        return results        

    affcorr_results = replace_filenames_with_results(affcorr_results)  
    return affcorr_results 

def affine_motion_correction(provenance, z, data, **kwargs):
    """
    Iterates over the sessions that were defined in multi_path.
    For each session, a reference image is generated by averaging the middle 50% of frames.
    The reference image is appended to the beginning of the stack and the StackReg affine motion correction
    runs using this images as the reference.
    Then we apply the transform discovered with the motion correction channel to the mc channel and the functional channel
    Then we chop the first frame back off and we ought to have affine motion corrected tiffs for both channels for all sessions.
    """
    from pystackreg import StackReg

    if not provenance['affine_motion_correction']:
        provenance['affine_motion_correction'] = {}

    if z not in provenance['affine_motion_correction']:
        provenance['affine_motion_correction'][z] = {}

    if 'filenames' in provenance['affine_motion_correction'][z]:
        affcorr_results = restore_affine_correction(provenance, z)   
        return provenance, affcorr_results  
            
    out_mcch = {}
    out_fch = {}
    
    output_dir = Path(provenance['output_dir']) / z
    output_dir.mkdir(exist_ok=True)
    prov = provenance['load_data']['args']
    ch_dict = prov['ch_dict']
    multi_path = [Path(session) for session in prov['multi_path']]
    
    affcorr_results_filenames = {}
    affcorr_results = {}
    for ch in ch_dict.values():
        affcorr_results_filenames[ch] = OrderedDict()
        affcorr_results[ch] = OrderedDict()

    for i, p in enumerate(multi_path):
        refsize = np.ceil(np.size(data[ch_dict['mc_ch']][i],0)/2)

        # create 'ref' which is the mean of the middle 50% of images (approximately)
        ref = np.mean( data[ch_dict['mc_ch']][i][ int(np.ceil(refsize/2)):int(np.ceil(refsize/2)+refsize), :, : ], 0 )
        toreg_mc = data[ch_dict['mc_ch']][i]

        # have to put ref as first frame because sr will not accept a reference image I created
        firstframetrick_mc = np.vstack([ref[np.newaxis,...], toreg_mc]) 
        
        #Affine transformation
        sr = StackReg(StackReg.AFFINE)
        out_aff = sr.register_stack(firstframetrick_mc, reference='first')
        out_mcch[i] = sr.transform_stack(firstframetrick_mc)
        affcorr_results[ch_dict['mc_ch']][i] = out_mcch[i][1:,:,:]

        mc_fname = output_dir / f'{p.name}_{ch_dict["mc_ch"]}-affcorr_results.ome.tif'
        affcorr_results_filenames[ch_dict['mc_ch']][i] = mc_fname
        imsave(mc_fname, affcorr_results[ch_dict['mc_ch']][i])

        toreg_fc = data[ch_dict['func_ch']][i]
        firstframetrick_fc = np.vstack([ref[np.newaxis,...], toreg_fc])
        out_fch[i] = sr.transform_stack(firstframetrick_fc)
        affcorr_results[ch_dict['func_ch']][i] = out_fch[i][1:,:,:] 


        func_fname = output_dir / f'{p.name}_{ch_dict["func_ch"]}-affcorr_results.ome.tif'
        affcorr_results_filenames[ch_dict['func_ch']][i] = func_fname
        imsave(func_fname, affcorr_results[ch_dict['func_ch']][i])


    #provenance['affine_motion_correction']['args'] = kwargs['__args_dict']
    provenance['affine_motion_correction'][z]['filenames'] = affcorr_results_filenames  

    _save_provenance(provenance)
    return provenance, affcorr_results 



