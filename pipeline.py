def _load_data(filenames, ch_dict, z, multi_crop, multi_crop_start, **kwargs):
    data = {}
    for ch in ch_dict.values(): 
        data[ch] = {z : OrderedDict()} 
        for i, _ in enumerate(filenames):
            filename = filenames[i][z][ch]
            imgs = imread(filename).squeeze()
    
            if multi_crop:
                data[ch][i] = imgs[multi_crop_start[i]:, :, :]  
            else:
                data[ch][i] = imgs

    return data                

